  <p>
	        <h3 id="premierenotion">Premières notions</a></h3></p>
  <p>L'algèbre linéaire généralise la notion de vecteur jusque dans leur manipulation. Les ensembles dans lesquels on travaille en algèbre linéaire sont des "<b>Espaces vectoriels</b>". 
  </p>
  <p>Un espace vectoriel c'est un peu comme un framework. On a un ensemble d'objets avec lesquels on peut jouer, selon certaines règles.   </p>
  <p>  </p>
  <p>  </p>
  <p> Mathématiquement un espace vectoriel est un ensemble dans lequel "vivent" des vecteurs répondant à certains critères. 
  </p>
  <p>
      <h3 id="notiondev">La notion d'espace vectoriel</a></h3></p>
  <p>
  </p>
  <p>Les axiomes :  </p>
  <p>
  
$\textbf{Fermeture de l'addition :}$ Pour tous vecteurs $\mathbf{u}$ et $\mathbf{v}$, $\mathbf{u} + \mathbf{v}$ est aussi un vecteur. </p>
  <p>
$\textbf{Fermeture pour la multiplication scalaire :}$ Pour tout scalaire $c$ et tout vecteur $\mathbf{v}$, $c \mathbf{v}$ est aussi un vecteur. </p>
  <p>
$\textbf{Commutativité de l'addition :}$ Pour tous vecteurs $\mathbf{u}$ et $\mathbf{v}$, $\mathbf{u} + \mathbf{v} = \mathbf{v} + \mathbf{u}$. </p>
  <p>
$\textbf{Associativité de l'addition :}$ Pour tous vecteurs $\mathbf{u}$, $\mathbf{v}$ et $\mathbf{w}$, $(\mathbf{u} + \mathbf{v}) + \mathbf{w} = \mathbf{u} + (\mathbf{v} + \mathbf{w})$. </p>
  <p>
$\textbf{Existence d'un élément neutre :}$ Il existe un vecteur nul $\mathbf{0}$ tel que $\mathbf{v} + \mathbf{0} = \mathbf{v}$ pour tout vecteur $\mathbf{v}$. </p>
  <p>
$\textbf{Existence d'un inverse :}$ Pour chaque vecteur $\mathbf{v}$, il existe un vecteur opposé $-\mathbf{v}$ tel que $\mathbf{v} + (-\mathbf{v}) = \mathbf{0}$. </p>
  <p>
$\textbf{Associativité pour la multiplication scalaire :}$ Pour tous scalaires $a$ et $b$, et tout vecteur $\mathbf{v}$, $(ab)\mathbf{v} = a(b\mathbf{v})$. </p>
  <p>
$\textbf{Distributivité sur les scalaires :}$ Pour tous scalaires $a$ et $b$, et tout vecteur $\mathbf{v}$, $(a + b)\mathbf{v} = a\mathbf{v} + b\mathbf{v}$. </p>
  <p>
$\textbf{Distributivité sur les vecteurs :}$ Pour tout scalaire $a$, et tous vecteurs $\mathbf{u}$ et $\mathbf{v}$, $a(\mathbf{u} + \mathbf{v}) = a\mathbf{u} + a\mathbf{v}$. </p>
  <p>
$\textbf{Existence d'un scalaire neutre :}$ Pour tout vecteur $\mathbf{v}$, $1 \cdot \mathbf{v} = \mathbf{v}$. </p>
