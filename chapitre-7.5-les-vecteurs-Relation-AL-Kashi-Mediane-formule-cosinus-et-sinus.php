   <h3>Relation d'Al-Kashi</h3>
   <p>
      Cette relation s'appelle aussi plus prosaïquement la loi des $cosinus$. Elle est une généralisation du théorème de Pythagore. 
   
   </p>
   <p>
       
   
      <h4>Loi des $cosinus$</h4>
      <p>
         La loi des $cosinus$ est une généralisation du théorème de Pythagore. On obtient une relation entre les longueur à l'aide du $cosinus$ d'un angle. <br/>
         Le principe est le suivant, appliqué au segment $[AB]$ : 
         
         $$
         \left\Vert AB\right\Vert^{2} = \left\Vert AC\right\Vert^{2}+\left\Vert BC\right\Vert^{2}-2\left\Vert AC\right\Vert.\left\Vert BC\right\Vert.\cos \gamma. 
         $$
          <img src="https://github.com/IamHenri/G1-Geometrie/blob/main/images/loi-des-cosinus.png?raw=true" width=50%>
          Noter que cette formule peut être extrapolée à chaque coté, du moment que l'on dispose de l'angle opposé. <br/>
          <strong>Exercices</strong> : Ecrire les deux autres formules pour les deux autres cotés. 
 
   <h3>Loi des $sinus$</h3>
</p>
<p>
   Dans ce même triangle, on a la relation, avec $A$, $B$ et $C$ distincts : 
           
         $$
         \dfrac{\sin \alpha}{BC} = \dfrac{\sin \beta}{AC} = \dfrac{\sin \gamma}{AB} 
         $$
</p>
   </p>
   <p>
   <h3>Théorème de la Médiane</h3>
   <p>Dans un triangle, la médiane issue d’un sommet est la droite qui passe par ce sommet et par le milieu du côté opposé.
   </p>
   <p>On appelle centre de gravité d’un triangle ABC l’unique point G tel que 
      $$
      \overrightarrow{GA} +\overrightarrow{GB} +\overrightarrow{GC} = \overrightarrow{0}
      $$
   </p>
   <p><h4>Théorème 1 : </h4>
      Les médianes d’un triangle sont concourantes (elles se coupent en un même point).
   
   </p>
   <p><h4>Théorème 2 : </h4>
      Leur point d’intersection est le centre de gravité.

   
   </p>
   <p><h4>Théorème 3 : </h4>
      Le centre de gravité est situé aux deux tiers d’une médiane en partant du sommet dont elle est issue.

   
   </p>
