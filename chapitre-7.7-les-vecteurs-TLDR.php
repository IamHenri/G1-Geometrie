   <h1>7 - Les vecteurs</h1>

   <h2>Première approche</h2>
   
</p>
<p>Comme on a pu le voir au début, un vecteur en deux ou trois dimension peut être représenté par une flèche munies de plusieurs caractéristiques : une origine, une direction, un sens et une norme. Cette représentation est aussi valable dans des dimensions supérieures. Ces vecteurs ont une signification physique concrète. 
</p>
<p>
Si on connait un vecteur, c'est qu'on peut le représenter dans un système de coordonnées.
   
   <h2>Deuxième approche</h2>
   Un vecteur est un élément d'un espace vectoriel. Bien que la première approche reste vraie, cette seconde représentation est plus générale et englobe la première. Voir les notes sur l'algèbre linéaire (encore à faire)
</p>
